#include <iostream>
using namespace std;
int main()
{
    cout << 5 / 2 << endl;        // integer output
    cout << (float)5 / 2 << endl; // type casting // float output
    cout << 5.0 / 2 << endl;      // float output
    cout << 5 / 2.0 << endl;      // float output
    cout << 5.0 / 2.0 << endl;    // float output

    return 0;
}